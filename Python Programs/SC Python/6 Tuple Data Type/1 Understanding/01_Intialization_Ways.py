t1 = ()
print(type(t1))             # <class 'tuple'>
print(t1)                   # ()

t2 = (1, 2, 3)
print(type(t2))             # <class 'tuple'>
print(t2)                   # (1, 2, 3)  

t3 = tuple((1, 2, 3))
print(type(t3))             # <class 'tuple'>
print(t3)                   # (1, 2, 3)