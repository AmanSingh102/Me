dict1 = {}
print(type(dict1))          # <class 'dict'>
print(dict1)                # {}

dict2 = {"Roll" : 30, "Name" : "Aman"}
print(type(dict2))          # <class 'dict'>
print(dict2)                # {'Roll': 30, 'Name': 'Aman'}

dict3 = dict({"ID" : "E001", "Name" : "Aman", "Salary" : 59.7})
print(type(dict3))          # <class 'dict'>
print(dict3)                # {'ID': 'E001', 'Name': 'Aman', 'Salary': 59.7}